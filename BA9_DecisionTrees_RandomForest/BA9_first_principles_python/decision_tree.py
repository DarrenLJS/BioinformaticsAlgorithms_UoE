# This class represents a node in the final results table
# It also generates a string describing the contents for printing


class Node:

    def __init__(self, left, right, name, cases, indexes, entropy):
        self.left = left    # left node
        self.right = right  # right node
        self.name = name    # name
        self.cases = cases  # cases
        self.indexes = indexes  # indexes of elements in teach set
        self.entropy = entropy   # entropy score of the node

    def totext(self):
        strg = "name: " + str(self.name) + "\n" + "left: " + str(self.left) + " right: " + str(self.right)
        strg = strg + "\n Entropy: " + "%0.2f" % self.entropy + "\n"

        strg = strg + self.indexestotext()

        for key, value in self.cases.items():
            strg = strg+" " + key + " " + str(value)
        return strg

    def indexestotext(self):

        s = "Indexes \n["

        for x in self.indexes:
            s = s+str(x)
            s = s+","
        s = s+"]\n"
        return s

# This class defines a binary split of the data
# In this code splits (generated by the exhaustive scan for conditions)


class MySplit:
    def __init__(self):
        self.entropies = []
        self.splits = []
        self.case = ""
        self.nodesfrom = []

    def createsplit(self, e, s, c, nf):
        self.entropies = e
        self.splits = s
        self.case = c
        self.nodesfrom = nf

    def getcombinedentropy(self):
        ent = 0

        # set this so combined entropy is 1 for empty element
        if len(self.entropies) == 0:
            return 1

        for value in self.entropies:
            ent = ent + value
        return ent / len(self.entropies)

# This is small utility class used to represent a split (a vector of indexes) and the node
# From which it was generated.


class MyASplit:
    def __init__(self, split, nodefrom):
        self.split = split
        self.nodefrom = nodefrom

