class MyDecisionTree_CART:
    def __init__(self, mydf, class_casenames, class_cases, class_column):
        self.mydata = mydf
        self.casenames = class_casenames
        self.cases = class_cases
        self.classcolumn = class_column
        self.labels = list(set(self.mydata.iloc[:, self.classcolumn]))

        self.tree = []

    def countdistinct(self, myclasses, classlabels):
        # noticed the variables passes are only used locally here

        results = {}

        for v in classlabels:
            results[v] = 0

        for i in myclasses:

            for x in classlabels:
                if x == i:
                    results[x] = results.get(x) + 1

        return results

    def buildtree(self):

        splits_table = []
        self.tree = []

        # create the first node
        count_classlabels_presplit = self.countdistinct(self.mydata.iloc[:, self.classcolumn], self.labels)

        entropy_pre = self.entropy(count_classlabels_presplit)
        print("Entropy for starting point: %d" % entropy_pre)

        # add the first node

        self.tree.append(
            Node(0, 0, "root", count_classlabels_presplit, self.mydata.iloc[:, self.classcolumn], entropy_pre))

        # OK now decide how to split the quantitative data into groups.

        err = self.mydata.iloc[:, self.casenames].values.tolist()

        bestsplit = self.buildtreelayer(MyASplit(err, 0))
        if bestsplit.getcombinedentropy() <= 0:
            print("Completed Successfully..")
            return 0
        # simple termination heuristic here
        if len(bestsplit.splits[0]) <= 2 or len(bestsplit.splits[1]) <= 2:
            print("Completed successfully.. splits too small to continue")
            return 0

        # otherwise just store the splits
        if len(bestsplit.splits[0]) > 2:
            splits_table.append(MyASplit(bestsplit.splits[0], bestsplit.nodesfrom[0]))
        if len(bestsplit.splits[1]) > 2:
            splits_table.append(MyASplit(bestsplit.splits[1], bestsplit.nodesfrom[1]))

        # add  a sanity check loop counter
        loop = 0

        while len(splits_table) > 0:

            loop += 1

            sp = splits_table.pop()

            bestsplit = self.buildtreelayer(sp)
            if bestsplit.getcombinedentropy() <= 0:
                print("Completed Successfully..")
                return 0
                # simple termination heuristic here
            if len(bestsplit.splits[0]) <= 2 or len(bestsplit.splits[1]) <= 2:
                print("Completed successfully.. splits too small to continue")
                return 0

            if loop > 0:
                print("Terminated due to insufficient time to solve the problem")
                return -1

                # otherwise just store the splits
            if len(bestsplit.splits[0]) > 2:
                splits_table.append(MyASplit(bestsplit.splits[0], bestsplit.nodesfrom[0]))
            if len(bestsplit.splits[1]) > 2:
                splits_table.append(MyASplit(bestsplit.splits[1], bestsplit.nodesfrom[1]))

        return -1

    def buildtreelayer(self, sp):

        best_split = MySplit()

        for i in self.cases:
            print("Considering case: " + self.mydata.columns[i])

            best_split2 = self.getBestSplit(sp.split, i, sp.nodefrom)
            if best_split2.getcombinedentropy() < best_split.getcombinedentropy():
                best_split = best_split2

        # Current best entropy
            print("Entropy for starting point: %.2f" % best_split.getcombinedentropy())

        # Now store this next node in the tree

        counts1 = self.countdistinct(self.mydata.iloc[best_split.splits[0], self.classcolumn], self.labels)
        counts2 = self.countdistinct(self.mydata.iloc[best_split.splits[1], self.classcolumn], self.labels)

        self.tree[sp.nodefrom].left = sp.nodefrom + 1
        self.tree[sp.nodefrom].right = sp.nodefrom + 2

        self.tree.append(Node(-1, -1, self.mydata.columns.values[best_split.case], counts1, best_split.splits[0], best_split.entropies[0]))
        # store the branches in the tree

        self.tree.append(Node(-1, -1, self.mydata.columns.values[best_split.case], counts2, best_split.splits[1], best_split.entropies[1]))
        # store the branches in the tree

        best_split.nodesfrom.clear()
        best_split.nodesfrom.append(self.tree[sp.nodefrom].left)
        best_split.nodesfrom.append(self.tree[sp.nodefrom].right)

        return best_split

    # Exhaustive partition search using rank order and a single boundary

    def getBestSplit(self, rows_to_include, casetoconsider, parentnode):

        subtable = self.mydata.iloc[rows_to_include, :]
        # print(tb.tabulate(subtable, headers='keys', tablefmt='psql'))

        sorted_indexes = subtable.iloc[:, casetoconsider]
        sorted_order = np.argsort(sorted_indexes)

        subtable = subtable.iloc[sorted_order, :]

        # print("sorted")
        # print(tb.tabulate(subtable, headers='keys', tablefmt='psql'))

        # simply store the best core here
        best_split = MySplit()

        # Now consider each possible split.
        for x in range(1, len(subtable) -1):

            # This is a slow way to do this but easy to see in code -we also only consider binary split here
            top_subtable =subtable.iloc[0: x, :]
            count_classlabels_top_subtable = self.countdistinct(top_subtable.iloc[:, self.classcolumn], self.labels)
            entropy_top = self.entropy(count_classlabels_top_subtable)

            bottom_subtable = subtable.iloc[x: len(subtable), :]
            count_classlabels_bottom_subtable = self.countdistinct(bottom_subtable.iloc[:, self.classcolumn], self.labels)
            entropy_bottom = self.entropy(count_classlabels_bottom_subtable)

            current_split = MySplit()
            current_split.splits.append(list(top_subtable.iloc[:, self.casenames]))
            current_split.splits.append(list(bottom_subtable.iloc[:, self.casenames]))

            current_split.entropies.append(entropy_top)
            current_split.entropies.append(entropy_bottom)
            current_split.case = casetoconsider
